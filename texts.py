PLAN_NAMES = {
    "base":     {"ru": "Base",     "en": "Base"},
    "ultimate": {"ru": "Ultimate", "en": "Ultimate"},
}


def get_text(key: str, lang: str = "ru", **kwargs) -> str:
    text = TEXTS.get(key, {}).get(lang, TEXTS.get(key, {}).get("ru", ""))

    # Подстановка названия плана
    if "plan" in kwargs:
        plan = kwargs["plan"]
        kwargs["plan_name"] = PLAN_NAMES.get(plan, {}).get(lang, plan.upper())

    # Специальная логика для buy_menu
    if key == "buy_menu":
        from payments import PRICE_ULTIMATE, PRICE_ULTIMATE_DISCOUNT
        has_base = kwargs.get("has_base", False)
        price = PRICE_ULTIMATE_DISCOUNT if has_base else PRICE_ULTIMATE
        discount = "~~699₽~~ " if has_base else ""
        note = (
            ("🎁 У вас есть Base — скидка 200₽ на Ultimate!" if lang == "ru"
             else "🎁 You own Base — 200₽ off Ultimate!")
            if has_base else ""
        )
        kwargs.update({"price": price, "discount": discount, "note": note})

    try:
        return text.format(**kwargs)
    except KeyError:
        return text
